// Operadores de Comparação em JavaScript

// Os operadores de comparação são usados para comparar dois valores e retornam um valor booleano (true ou false).

// Igualdade (==)
// Compara se os valores são iguais, sem considerar o tipo.
console.log("5 == '5':", 5 == '5'); // true

// Estritamente igual (===)
// Compara se os valores e os tipos são iguais.
console.log("5 === '5':", 5 === '5'); // false
console.log("5 === 5:", 5 === 5); // true

// Diferente (!=)
// Compara se os valores são diferentes, sem considerar o tipo.
console.log("5 != '5':", 5 != '5'); // false

// Estritamente diferente (!==)
// Compara se os valores e/ou tipos são diferentes.
console.log("5 !== '5':", 5 !== '5'); // true

// Maior que (>)
console.log("10 > 5:", 10 > 5); // true

// Menor que (<)
console.log("10 < 5:", 10 < 5); // false

// Maior ou igual (>=)
console.log("10 >= 10:", 10 >= 10); // true

// Menor ou igual (<=)
console.log("5 <= 10:", 5 <= 10); // true

// Resumo dos principais operadores de comparação:
// ==   : igual (compara apenas o valor)
// ===  : estritamente igual (compara valor e tipo)
// !=   : diferente (compara apenas o valor)
// !==  : estritamente diferente (compara valor e tipo)
// >    : maior que
// <    : menor que
// >=   : maior ou igual
// <=   : menor ou igual

// Esses operadores são fundamentais para estruturas de decisão, como if, while, for, etc.
