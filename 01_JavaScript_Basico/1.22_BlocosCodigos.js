// Blocos de código em JavaScript

// Um bloco de código é um conjunto de instruções agrupadas entre chaves { }.
// Eles são usados para delimitar o escopo de estruturas como funções, condicionais, loops, etc.

// Exemplo com if:
let idade = 20;

if (idade >= 18) {
    // Este é um bloco de código
    console.log("Você é maior de idade.");
} else {
    // Outro bloco de código
    console.log("Você é menor de idade.");
}

// Exemplo com função:
function mostrarMensagem() {
    // Bloco de código da função
    console.log("Olá, este é um bloco de código dentro de uma função!");
}

mostrarMensagem();

// Exemplo com laço for:
for (let i = 0; i < 3; i++) {
    // Bloco de código do loop
    console.log("Repetição número: " + i);
}

// Resumindo:
// - Blocos de código são delimitados por { }.
// - Eles definem o escopo de variáveis e instruções.
// - São essenciais para organizar e estruturar o código em JavaScript.
