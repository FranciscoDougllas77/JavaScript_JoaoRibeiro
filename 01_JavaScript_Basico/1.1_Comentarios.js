// Comentários em JavaScript são usados para explicar o código e facilitar a leitura,
// tanto para você quanto para outras pessoas que possam trabalhar no mesmo projeto.

// Existem dois tipos principais de comentários em JavaScript:

// 1. Comentário de linha única:
// Use duas barras (//) para comentar uma única linha.
console.log("Este código será executado"); // Isto é um comentário de linha única

// 2. Comentário de múltiplas linhas:
// Use /* e */ para comentar várias linhas.
 /*
   Este é um comentário
   de múltiplas linhas.
   Pode ser usado para explicar trechos maiores do código.
 */
console.log("Comentários não afetam a execução do código");

// Dica: Use comentários para explicar o "porquê" de uma decisão, não apenas o "o quê".
