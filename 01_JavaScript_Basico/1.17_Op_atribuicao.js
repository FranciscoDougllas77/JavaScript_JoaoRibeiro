// Operadores de Atribuição em JavaScript

// Os operadores de atribuição são usados para atribuir valores a variáveis.
// O operador de atribuição mais comum é o igual (=), mas existem outros operadores combinados que facilitam operações matemáticas e lógicas.

// Atribuição simples (=)
let x = 10;
console.log("Atribuição simples:", x); // 10

// Atribuição com adição (+=)
x += 5; // Equivale a: x = x + 5
console.log("Após x += 5:", x); // 15

// Atribuição com subtração (-=)
x -= 3; // Equivale a: x = x - 3
console.log("Após x -= 3:", x); // 12

// Atribuição com multiplicação (*=)
x *= 2; // Equivale a: x = x * 2
console.log("Após x *= 2:", x); // 24

// Atribuição com divisão (/=)
x /= 4; // Equivale a: x = x / 4
console.log("Após x /= 4:", x); // 6

// Atribuição com módulo (%=)
x %= 4; // Equivale a: x = x % 4
console.log("Após x %= 4:", x); // 2

// Atribuição com exponenciação (**=)
x **= 3; // Equivale a: x = x ** 3
console.log("Após x **= 3:", x); // 8

// Resumo dos principais operadores de atribuição:
// =   : atribuição simples
// +=  : atribuição com adição
// -=  : atribuição com subtração
// *=  : atribuição com multiplicação
// /=  : atribuição com divisão
// %=  : atribuição com módulo
// **= : atribuição com exponenciação

// Esses operadores tornam o código mais limpo e fácil de ler ao realizar operações e atualizar valores de variáveis.
