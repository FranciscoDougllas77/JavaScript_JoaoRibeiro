// Statements (instruções) em JavaScript são comandos que dizem ao navegador para fazer algo.
// Cada statement geralmente termina com ponto e vírgula (;), embora em muitos casos o JavaScript consiga interpretar o final da instrução sem ele.

// Exemplos de statements:
let nome = "João"; // declaração de variável (statement)
console.log(nome); // chamada de função (statement)
if (nome === "João") { // statement condicional
    console.log("Olá, João!");
}

// Existem vários tipos de statements em JavaScript, como:
// - Declaração de variáveis (let, const, var)
// - Condicionais (if, else, switch)
// - Laços de repetição (for, while, do...while)
// - Expressões (atribuições, chamadas de função, etc.)

// Dica: Escrever cada statement em uma linha facilita a leitura do código.
