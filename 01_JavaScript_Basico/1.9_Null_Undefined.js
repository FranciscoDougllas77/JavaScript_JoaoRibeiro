
// Exemplo de Undefined:
// Uma variável declarada, mas não inicializada, tem o valor undefined por padrão.
let variavelIndefinida;
console.log("Valor de variavelIndefinida:", variavelIndefinida); // Saída: undefined

// Exemplo de Null:
// Null é atribuído explicitamente para indicar ausência intencional de valor.
let variavelNula = null;
console.log("Valor de variavelNula:", variavelNula); // Saída: null

// Diferença entre undefined e null:
// undefined geralmente significa "ainda não atribuído".
// null significa "ausência intencional de valor".
console.log("Tipo de variavelIndefinida:", typeof variavelIndefinida); // Saída: undefined
console.log("Tipo de variavelNula:", typeof variavelNula); // Saída: object
