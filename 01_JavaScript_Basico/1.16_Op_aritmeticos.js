// Operadores Aritméticos em JavaScript

// Os operadores aritméticos são usados para realizar operações matemáticas entre valores numéricos.

// Soma (+)
let a = 10;
let b = 5;
let soma = a + b;
console.log("Soma:", soma); // 15

// Subtração (-)
let subtracao = a - b;
console.log("Subtração:", subtracao); // 5

// Multiplicação (*)
let multiplicacao = a * b;
console.log("Multiplicação:", multiplicacao); // 50

// Divisão (/)
let divisao = a / b;
console.log("Divisão:", divisao); // 2

// Módulo (%) - Resto da divisão
let resto = a % b;
console.log("Resto da divisão:", resto); // 0

// Exponenciação (**)
let potencia = a ** b;
console.log("Exponenciação:", potencia); // 100000

// Incremento (++)
let c = 1;
c++;
console.log("Incremento:", c); // 2

// Decremento (--)
c--;
console.log("Decremento:", c); // 1

// Resumo:
// + : soma
// - : subtração
// * : multiplicação
// / : divisão
// % : módulo (resto da divisão)
// ** : exponenciação
// ++ : incremento
// -- : decremento
